/**
 * Formats export data as JSON
 */
export function formatJSON(data: ExportData): string {
  const exportData = {
    format: 'agenticwork-chat-export',
    version: '1.0',
    exportedAt: data.exportedAt,
    session: {
      id: data.session.id,
      title: data.session.title,
      createdAt: data.session.createdAt,
      updatedAt: data.session.updatedAt,
      metadata: data.session.metadata,
      messages: data.session.messages.map(message => ({
        id: message.id,
        role: message.role,
        content: message.content,
        timestamp: message.timestamp,
        tokenUsage: message.tokenUsage,
        toolCalls: message.toolCalls,
        mcpCalls: message.mcpCalls
      }))
    },
    exportOptions: data.exportOptions,
    userInfo: data.userInfo,
    statistics: {
      totalMessages: data.session.messages.length,
      userMessages: data.session.messages.filter(m => m.role === 'user').length,
      assistantMessages: data.session.messages.filter(m => m.role === 'assistant').length,
      systemMessages: data.session.messages.filter(m => m.role === 'system').length,
      totalTokens: data.session.messages.reduce((sum, m) => sum + (m.tokenUsage?.totalTokens || 0), 0),
      averageTokensPerMessage: Math.round(
        data.session.messages.reduce((sum, m) => sum + (m.tokenUsage?.totalTokens || 0), 0) / 
        data.session.messages.length || 0
      ),
      conversationDuration: Math.round(
        (new Date(data.session.updatedAt).getTime() - new Date(data.session.createdAt).getTime()) / 60000
      ) // minutes
    }
  };
  
  return JSON.stringify(exportData, null, 2);
}

/**
 * Helper function to format message content for HTML export
 */
function formatContentForHTML(content: string): string {
  return content
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;')
    .replace(/```(\w*)\n([\s\S]*?)```/g, '<pre><code>$2</code></pre>')
    .replace(/`([^`]+)`/g, '<code>$1</code>')
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
    .replace(/\*(.*?)\*/g, '<em>$1</em>')
    .replace(/\n/g, '<br>');
}