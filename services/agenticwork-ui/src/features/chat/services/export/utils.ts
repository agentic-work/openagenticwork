/**
 * Formats export data as Markdown
 */
export function formatMarkdown(data: ExportData): string {
  const { session, exportOptions, userInfo } = data;
  
  let markdown = '';
  
  // Header
  markdown += `# ${session.title}\n\n`;
  
  // Metadata
  if (exportOptions.includeMetadata) {
    markdown += `## Session Information\n\n`;
    markdown += `- **Created:** ${new Date(session.createdAt).toLocaleString()}\n`;
    markdown += `- **Last Updated:** ${new Date(session.updatedAt).toLocaleString()}\n`;
    markdown += `- **Message Count:** ${session.messages.length}\n`;
    
    if (session.metadata) {
      if (session.metadata.model) markdown += `- **Model:** ${session.metadata.model}\n`;
      if (session.metadata.totalTokens) markdown += `- **Total Tokens:** ${session.metadata.totalTokens.toLocaleString()}\n`;
      if (session.metadata.totalCost) markdown += `- **Estimated Cost:** $${session.metadata.totalCost.toFixed(4)}\n`;
    }
    
    if (userInfo?.name) markdown += `- **User:** ${userInfo.name}\n`;
    markdown += `- **Exported:** ${new Date(data.exportedAt).toLocaleString()}\n\n`;
  }
  
  // Messages
  markdown += `## Conversation\n\n`;
  
  session.messages.forEach((message, index) => {
    const roleEmoji = message.role === 'user' ? 'üë§' : message.role === 'assistant' ? 'ü§ñ' : '‚öôÔ∏è';
    
    markdown += `### ${roleEmoji} ${message.role.charAt(0).toUpperCase() + message.role.slice(1)}`;
    
    if (exportOptions.includeTimestamps) {
      markdown += ` *(${new Date(message.timestamp).toLocaleString()})*`;
    }
    
    if (exportOptions.includeTokenUsage && message.tokenUsage) {
      markdown += ` ‚Ä¢ ${message.tokenUsage.totalTokens} tokens`;
    }
    
    markdown += `\n\n${message.content}\n\n`;
    
    if (index < session.messages.length - 1) {
      markdown += `---\n\n`;
    }
  });
  
  // Footer
  markdown += `\n---\n\n*Generated by AgenticWork Chat ‚Ä¢ Professional AI Assistant*\n`;
  
  return markdown;
}

